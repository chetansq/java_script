// Comparing Strings [5 String]

{
    let string1 = 'Hello';
    let string2 = 'hello';
    let string3 = 'hello_';
    let string4 = 'hello world';
    let string5 = 'Hello_world';

    let str1 = string1 == string2;
    console.log(str1);

    let str2 = string1 === string2;
    console.log(str2);

    let str3 = string3 > string2;
    console.log(str3);

    let str4 = string4 > string5;
    console.log(str4);

    let str5 = string3 != string2;
    console.log(str5);
}
